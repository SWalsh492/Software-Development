
public class JavaSwitch1
{
	public static void main(String[] args)
	{
		int num1 = 69;

		switch(num1)
		{
			case 100:
				System.out.println("The value of the variable is 100!");
				break;

			default:
				System.out.println("The value of the varaible is not 100!");



		}
	}
}