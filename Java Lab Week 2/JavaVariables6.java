public class JavaVariables6
{
	public static void main(String[] args)
	{
		byte minByValue = -128;
		byte maxByValue = 127;
		short minShValue = -32768;
		short maxShValue = 32767;
		int minIntValue = -2147483647;
		int maxIntValue = 214783648;
		long minLValue = -9223372036854775808L;
		long maxLValue = 9223372036854775807L;
		double minDValue = 4.9E-324;
		double maxDValue = 1.7976931348623157E308;

		System.out.println("Min Byte Value is: " + minByValue);
		System.out.println("Max Byte Value is: " + maxByValue);
		System.out.println("Min short Value is: " + minShValue);
		System.out.println("Max short Value is: " + maxShValue);
		System.out.println("Min Int Value is: " + minIntValue);
		System.out.println("Max Int Value is: " + maxIntValue);
		System.out.println("Min Long Value is: " + minLValue);
		System.out.println("Max Long Value is: " + maxLValue);



	}

}