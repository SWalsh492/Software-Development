public class JavaVariables13
{
	public static void main(String[] args)
	{
		double pi = 3.1415;
		double radius = 5;
		double area = pi*(radius*radius);
		double diameter = radius + radius;
		double circum = pi*(radius*2);


		System.out.println("The area of a circle");
		System.out.println("With a radius of " + radius);
		System.out.println("is: " + area);
		System.out.println("\n");
		System.out.println("The diameter of a circle");
		System.out.println("With a radius of " + radius);
		System.out.println("is: " + diameter);
		System.out.println("\n");
		System.out.println("The circumference of a circle");
		System.out.println("With a radius of " + radius);
		System.out.println("is: " + circum);






	}
}