public class Data
{
	private String name;
	private int age;
	private String address;
	private String phone;

	// Setters
	public void setName(String dataName)
	{
		name = dataName;
	}

	public void setAge(int year)
	{
		age = year;
	}

	public void setAddress(String residence)
	{
		address = residence;
	}

	public void setPhone(String phoneNumber)
	{
		phone = phoneNumber;
	}

	// Getters

	public String getName()
	{
		return name;
	}

	public int getAge()
	{
		return age;
	}

	public String getAddress()
	{
		return address;
	}

	public String getPhone()
	{
		return phone;
	}



}