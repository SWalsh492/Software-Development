public class Toy
{
	private String name;
	private double price;

	public void setName(String nameEntered)
	{
		name = nameEntered;
	}

	public String getName()
	{
		return name;
	}

	public void setPrice(double cost)
	{
		price = cost;
	}

	public double getPrice()
	{
		return price;
	}
}